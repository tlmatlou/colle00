#include "ft_putchar.c"


void rush(int x, int y)
{
  char c;
  
  int a =0;
  while ( a < y)
  {
	  int b =0;
      while ( b < x)
	  {
        if ((a == 0 || a == y-1) && (b == 0 || b== x-1))
		{
          c='o';
		}
        else if (a == 0 || a == y-1)
		{
          c = '-';
		}
        else if (b ==0 || b == x-1)
		{
          c = '|';
		}
        else
		{
		   	c = ' ';
		}
          ft_putchar(c);
		  b++;
	  }

	  a++;
      ft_putchar('\n');
  }
}
